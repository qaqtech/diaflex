    public ArrayList getPktList() {
        return pktList;
    }

    public void setReportNme(String reportNme) {
        this.reportNme = reportNme;
    }

    public String getReportNme() {
        return reportNme;
    }

    public void setSrchTypList(ArrayList srchTypList) {
        this.srchTypList = srchTypList;
    }

    public ArrayList getSrchTypList() {
        return srchTypList;
    }

